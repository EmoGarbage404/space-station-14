    }

    public void Update(Entity<AnalysisConsoleComponent> ent)
    {
        Logger.Debug("Ok we got a message here:");
        Logger.Debug($"arti not null : {_artifactAnalyzer.TryGetArtifactFromConsole(ent, out _)}");
    }
}

